package com.tokioschool.holamundo;

/*
  Operadores combinados
 */
public class OperadoresCombinados {
    public static void main(String args[]) {
        int numero1 = 11;
        int numero2 = 2;

        // Estas tres opciones son equivalentes
        numero1++;
        numero1 = numero1 + 1;
        numero1 += 1;
        System.out.println(numero1);
        numero1 -= 4;
        System.out.println(numero1);
    }
}
