public class OperadoresComparacion {
    public static void main(String args[]) {
        int cantidadProductos = 10;
        int precioFinal = 20;

        System.out.println(cantidadProductos == precioFinal);
        System.out.println(cantidadProductos != precioFinal);
        System.out.println(cantidadProductos > precioFinal);
        System.out.println(cantidadProductos >= precioFinal);
        System.out.println(cantidadProductos < precioFinal);
        System.out.println(cantidadProductos <= precioFinal);
    }
}
