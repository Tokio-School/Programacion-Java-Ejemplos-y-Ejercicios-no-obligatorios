package ejem01.clases;

public class Persona {
	String nombre;
	int edad;
	boolean casado;
	
	
	public Persona() {
	}


	public Persona(String nombre) {
		this.nombre = nombre;
	}


	public Persona(String nombre, int edad, boolean casado) {
		this.nombre = nombre;
		this.edad = edad;
		this.casado = casado;
	}
	
	
	
	
	
	
	
}
